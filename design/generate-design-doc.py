def H2(text):
    return doc.add_heading(text, level=2)

def H3(text):
    return doc.add_heading(text, level=3)

def B(text):
    return doc.add_paragraph(text)

def BL(text):
    return doc.add_paragraph(text, style='List Bullet')

def BN(text):
    """Bullet without marker (flat paragraph)."""
    p = doc.add_paragraph(text)
    p.paragraph_format.left_indent = Cm(1.27)
    return p

def BOLD(text):
    p = doc.add_paragraph()
    p.add_run(text).bold = True
    return p

def MONO(text):
    """Monospace for ASCII diagrams."""
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(0)
    p.paragraph_format.space_after = Pt(0)
    run = p.add_run(text)
    run.font.name = 'Consolas'
    run.font.size = Pt(8)
    return p

def TABLE(headers, rows, col_widths=None):
    t = doc.add_table(rows=len(rows) + 1, cols=len(headers))
    t.style = 'Table Grid'
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    for i, h in enumerate(headers):
        cell = t.rows[0].cells[i]
        cell.text = ''
        p = cell.paragraphs[0]
        r = p.add_run(h)
        r.bold = True
        r.font.size = Pt(10)
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        # Header background
        shading = OxmlElement('w:shd')
        shading.set(qn('w:fill'), '0D1117')
        shading.set(qn('w:val'), 'clear')
        cell._element.get_or_add_tcPr().append(shading)
        r.font.color.rgb = RGBColor(0xE6, 0xED, 0xF3)
    for ri, row in enumerate(rows):
        for ci, val in enumerate(row):
            cell = t.rows[ri + 1].cells[ci]
            cell.text = ''
            p = cell.paragraphs[0]
            r = p.add_run(str(val))
            r.font.size = Pt(10)
    if col_widths:
        for i, w in enumerate(col_widths):
            for row in t.rows:
                row.cells[i].width = Cm(w)
    return t

def COLOR_SWATCH(name, hex_val):
    p = doc.add_paragraph()
    p.paragraph_format.space_after = Pt(2)
    r = p.add_run(f'  ■  {name}: #{hex_val}')
    r.font.name = 'Consolas'
    r.font.size = Pt(9)
    return p

def LINK(paragraph, url, display_text=None):
    part = paragraph.part
    r_id = part.relate_to(url, docx.opc.constants.RELATIONSHIP_TYPE.HYPERLINK, is_external=True)
    hl = OxmlElement('w:hyperlink')
    hl.set(qn('r:id'), r_id)
    nr = OxmlElement('w:r')
    rPr = OxmlElement('w:rPr')
    c = OxmlElement('w:color')
    c.set(qn('w:val'), '0563C1')
    rPr.append(c)
    u = OxmlElement('w:u')
    u.set(qn('w:val'), 'single')
    rPr.append(u)
    nr.append(rPr)
    nr.text = display_text or url
    hl.append(nr)
    paragraph._element.append(hl)

def PAGEBREAK():
    doc.add_page_break()

